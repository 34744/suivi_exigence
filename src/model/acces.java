package model;

public class acces {

	String ulis;
	String mdp;
	
	
	
	public acces(String ulis, String mdp) {
		super();
		this.ulis = ulis;
		this.mdp = mdp;
	}
	public acces() {
		super();
	}
	public String getUlis() {
		return ulis;
	}
	public void setUlis(String ulis) {
		this.ulis = ulis;
	}
	public String getMdp() {
		return mdp;
	}
	public void setMdp(String mdp) {
		this.mdp = mdp;
	}
	
	
}
